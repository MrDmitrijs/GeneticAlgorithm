package genetic.Common;

public class Details {

    private String fullBinaryCode;
    private double valueOfX;
    private double valueOfY;
    private double valueOfFitnessFunction;

    public String getFullBinaryCode() {
        return fullBinaryCode;
    }

    public void setFullBinaryCode(String fullBinaryCode) {
        this.fullBinaryCode = fullBinaryCode;
    }

    public double getValueOfX() {
        return valueOfX;
    }

    public void setValueOfX(double valueOfX) {
        this.valueOfX = valueOfX;
    }

    public double getValueOfY() {
        return valueOfY;
    }

    public void setValueOfY(double valueOfY) {
        this.valueOfY = valueOfY;
    }

    public double getValueOfFitnessFunction() {
        return valueOfFitnessFunction;
    }

    public void setValueOfFitnessFunction(double valueOfFitnessFunction) {
        this.valueOfFitnessFunction = valueOfFitnessFunction;
    }
}
